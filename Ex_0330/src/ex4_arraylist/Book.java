package ex4_arraylist;

public class Book {
	
	private String title;
	private int price;

	protected Book (String title, int price) {
		
		this.title = title;
		this.price = price;
		
	}
	
	// ############### Getter-Setter ###############
	
	public String getTitle() {
		return title;
	}
		
	public int getPrice() {
		return price;
	}
		
	// ##################################
	
	
	
}
